print("Введите через пробел коэффициенты a, b, c для квадратного уравнения ax^2 + bx + c = 0")
a, b, c, = map(int, input().split())
D = b ** 2 - 4 * a * c
if D > 0:
    x1 = (-b + D ** 0.5) / (2 * a)
    x2 = (-b + D ** 0.5) / (2 * a)
    print(f"Корни уравнения: {x1} и {x2}")
elif D == 0:
    x1 = -b / (2 * a)
    print(f"Корень уравнения: {x1}")
else:
    print("Нет корней")
